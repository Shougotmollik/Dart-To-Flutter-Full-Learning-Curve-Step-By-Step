void main() {
// final keyword

  final cityName = "Tokeyo";
  final String CountryName = "Japan";
  print(cityName);
  print(CountryName);

  //constant keyword



  const PI = 3.1416;
  print(PI);
  const double Gravity = 9.8;
  print(Gravity);
}

class Circle {
  final color = "Red";
  static const PI = 3.1416;
