void main() {
//string

  String name = "jhon";
  var city = "tokeyo";
  print(name);
  print(city);

//integer
  int age = 21;
  print(age);
  var x = 221; //it is inferred as integer automatically by compiler
  print(x);

//double

  double percentage = 93.4;
  print(percentage);
  var temparature = 13.56;
  print(temparature);

//Boolean

  bool isValid = true;
  var isAlive = false;
  print(isValid);
  print(isAlive);

  //Note: All data types in Dart are objects.
  //Therefore ,their initial value is by deafult "null"
}
